package Hashing;
import java.util.*;
 
public class linkedHashSet {
    public static void main(String[] args) {
        // Creating a linked hash set
        LinkedHashSet<String> lhs = new LinkedHashSet<>();

        lhs.add("Apple");
        lhs.add("Banana");
        lhs.add("Cherry");
        lhs.add("Date");
        lhs.add("Elderberry");

        System.out.println("LinkedHashSet: " + lhs);



    }
    
}
